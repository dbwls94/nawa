package org.nawa.datatype;

public class Tuple2<V1, V2> implements ITuple{
	private V1 value1;
	private V2 value2;

	public Tuple2(V1 value1, V2 value2) {
		this.value1 = value1;
		this.value2 = value2;
	} // INIT

	public V1 getV1() {
		return this.value1;
	} // getV1

	public V2 getV2() {
		return this.value2;
	} // getV2
} // class