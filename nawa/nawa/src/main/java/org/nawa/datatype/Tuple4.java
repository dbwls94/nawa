package org.nawa.datatype;

public class Tuple4<V1, V2, V3, V4> extends Tuple3<V1, V2, V3>{
	private V4 value4;
	
	public Tuple4(V1 value1, V2 value2, V3 value3, V4 value4) {
		super(value1, value2, value3);
		this.value4=value4;
	} //INIT

	public V4 getV4(){
		return this.value4;
	} //getV4
} //class