package org.nawa.entity;

import org.nawa.common.json.JsonColumn;
import org.nawa.common.json.JsonSerializable;

public class NavBarEntity extends JsonSerializable{
	@JsonColumn(key="username")
	private String username;
	
	@JsonColumn(key="handlingProjectCount")
	private int handlingProjectCount;
	
	@JsonColumn(key="notificationCount")
	private int notificationCount;

	public NavBarEntity() {
	} // INIT

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public int getHandlingProjectCount() {
		return handlingProjectCount;
	}

	public void setHandlingProjectCount(int handlingProjectCount) {
		this.handlingProjectCount = handlingProjectCount;
	}

	public int getNotificationCount() {
		return notificationCount;
	}

	public void setNotificationCount(int notificationCount) {
		this.notificationCount = notificationCount;
	}
	
	
} // class